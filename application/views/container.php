<script type="text/javascript">
$(document).ready(function()
{
	countValue();
	$("#decalpriceform-sizewidth,#decalpriceform-sizeheight,#decalpriceform-quantity,#decalpriceform-decaltype,#decalpriceform-isextrusion").change(function(){
		countValue();
	});
});
function countValue(){
	if ($("#decalpriceform-sizewidth").val() > 0 
        && $("#decalpriceform-sizeheight").val() > 0 
        && $("#decalpriceform-quantity").val() > 0
    ) {

        $("#order-btn").removeClass("hidden");
    	$.get("<?php echo base_url('Home/calculate'); ?>", {
            sizeWidth: $("#decalpriceform-sizewidth").val(), 
            sizeHeight: $("#decalpriceform-sizeheight").val(), 
            quantity: $("#decalpriceform-quantity").val(), 
            decalType: $("#decalpriceform-decaltype").val(), 
            isExtrusion: $("#decalpriceform-isextrusion").val()
        }, function(resp) {
            if (resp) {
            	var res = jQuery.parseJSON(resp);
                $("#unit_price").html(res.unit_price);
                $("#total_order").html(res.total_order);
                $("#summary_size").html(res.summary_size);
                $("#summary_quantity").html(res.summary_quantity);
                $("#summary_decal_type").html(res.summary_decal_type);
            }
    	});
	};
}

</script>